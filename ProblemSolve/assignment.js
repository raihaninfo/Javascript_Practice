// feet to mile convert

/*
 * 1 feet = 0.0001893939393939394 mile
 *  1 mile = 5280 feet
 * 10000/5280
 */


function feetToMile(feet) {
    let feetValue = feet / 5280
    let feetRound = Math.round(feetValue)
    return feetRound
}

